# ========================================输出↓=========================================
# 输出Hello World
print("Hello World!")
# 使用逗号分开，输出结果遇到逗号，会输出一个空格
print('hello', 'world', '!')
# print也可以输出整形
print(300)
# 也可以是算式
print(100 + 200)
# 或者
print("100 + 200 =", 100 + 200)

# ========================================输入↓=========================================
name = input()
print("Hello,", name)
# 加输入提示
name = input('Please Input your name: \n')
print('Hello,', name)

